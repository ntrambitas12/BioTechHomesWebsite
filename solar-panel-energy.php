            <p>
            The sun is constantly giving out particles called photons. The solar panel turns the photons into electrons which can be used as DC energy. Then, the DC power goes through an inverter which changes it into AC power. AC power is then able to go straight into your computers, televisions, and other electronic devices.
Because the sun isn’t out in the night, a net energy meter is used. The net energy meter is used to track all of the power being used and to save power for later when it is either cloudy or the sun isn’t out.
For the most part, anyone could install their own solar panels on their home, but to refrain from injury it is best to contact a personal contractor.

